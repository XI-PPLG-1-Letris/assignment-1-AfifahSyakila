//penjumlahan
const tambah = (a,b) => {
    return a + b;
}
console.log(tambah(5,5));

//pengurangan
const kurang = (a,b) => {
    return a - b;
}
console.log(kurang(5,3));

//perkalian
const kali = (a,b) => {
    return a * b;
}
console.log(kali(2,4));

//pembagian
const bagi = (a,b) => {
    return a / b;
}
console.log(bagi(6,2));

//perpangkatan
const pangkat = (a,b) => {
    return a ** b;
}
console.log(pangkat(2,3))